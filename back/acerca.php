                     La Colmena es una empresa familiar que tiene sus orígenes en el año de 1945 en la Ciudad de México. La tradición de esta empresa se basa en el proceso de la cera de abeja, 
                     la cual es materia prima de muchas industrias tales como la alimenticia, cosmética, textil y farmacéutica. 
                     Además de estos usos, la cera de abeja es producto base en la fabricación de velas y cirios. Es así como La Colmena decide explorar este mercado y funda su fábrica.
                     <br /><br />
                    El método de inmersión es el toque artesanal de la producción, el cual consiste en sumergir el pabilo en la cera capa por capa hasta que el cirio obtiene el diámetro deseado. 
                    Este proceso brinda certeza al consumidor de que cada cirio se consume en su totalidad y de manera uniforme sin humear.
                    <br /><br />
                    ﻿   Otra característica son los diferentes tipos de decorado artesanal elaborados por manos mexicanas para hacer de nuestros cirios un producto único en el mercado.
                    <br /><br />
                    ​   Actualmente La Colmena ha diversificado sus productos desde la simple elaboración de la vela hasta desarrollos para la industria cosmética. Otro producto de mucho éxito desde hace 20 años son las veladoras y velas a la Santísima Providencia. 
                    La Colmena fue la empresa pionera en fabricar velas y veladoras de parafina manteniendo altos estándares de calidad e innovando con empaques y accesorios funcionales. Nos reconocen por dos presentaciones, la famosa “mochila amarilla” y la “caja dorada”.
                    <br /><br />
